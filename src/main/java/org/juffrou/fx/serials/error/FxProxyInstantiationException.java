package org.juffrou.fx.serials.error;

public class FxProxyInstantiationException extends RuntimeException {

	private static final long serialVersionUID = -4908328236816185841L;

	public FxProxyInstantiationException() {
		super();
	}

	public FxProxyInstantiationException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FxProxyInstantiationException(String message, Throwable cause) {
		super(message, cause);
	}

	public FxProxyInstantiationException(String message) {
		super(message);
	}

	public FxProxyInstantiationException(Throwable cause) {
		super(cause);
	}

	
}
