public class FxSerialsProxyAlreadExistsException extends Exception {

	private static final long serialVersionUID = 1888844272081046882L;

	public FxSerialsProxyAlreadExistsException() {
		super();
	}

	public FxSerialsProxyAlreadExistsException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FxSerialsProxyAlreadExistsException(String message, Throwable cause) {
		super(message, cause);
	}

	public FxSerialsProxyAlreadExistsException(String message) {
		super(message);
	}

	public FxSerialsProxyAlreadExistsException(Throwable cause) {
		super(cause);
	}

	
}
