package org.juffrou.fx.serials.error;

public class FxSerialsProxyCreationException extends RuntimeException {

	private static final long serialVersionUID = 7861041646277694695L;

	public FxSerialsProxyCreationException() {
		super();
	}

	public FxSerialsProxyCreationException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FxSerialsProxyCreationException(String message, Throwable cause) {
		super(message, cause);
	}

	public FxSerialsProxyCreationException(String message) {
		super(message);
	}

	public FxSerialsProxyCreationException(Throwable cause) {
		super(cause);
	}

	
}
